package Casestudy;

public class Employee {
String name;
int age;
String m1;
String email;
public String getM1() {
	return m1;
}
public void setM1(String m1) {
	this.m1 = m1;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}

}
